
public class WhileApp {

	public static void main(String[] args) {
		
		int n=1;
		
		while (n <= 10){
			//code to repeat
			System.out.println(n);
			if( n==5 ){ //se indeplineste 10 ori
				System.out.println("-----------------------"); //se indeplineste doar odata
			}
			
			//end instructions
			n++; //n sare cu 1 sau n+=2 - sare cu 2
		}
	
		
	}

}
